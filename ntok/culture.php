				<h4>역사</h4>
				<p>
				국립창극단은 1962년 창단 이후 '창극의 정형화와 국제화'를 목표로 하고 있다. 순수 전통공연과 전통을 기반으로 한 새로운 방향의 공연을 통하여 창극의 활성화를 꾀하고 있다. 창극의 정형화를 위해 4대가의 극본을 정립하고, 1998년부터는 4대가를 완판으로 공연하고 있으며, 판소리 다섯마당을 영·불·일어로 번역 출간하는 등 많은 외국인들에게 대표적인 한국 전통예술로 알려진 창극의 길라잡이 역할을 톡톡히 해왔다. 또한 1984년부터 진행된 토요상설무대 <완창 판소리>는 명창들을 통해 고유한 우리 소리의 멋을 전하고 있다.
